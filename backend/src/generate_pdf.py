from reportlab.lib.pagesizes import A4
from reportlab.pdfgen import canvas
from reportlab.lib.units import mm
import os

output_dir=r"C:\Users\akank\Desktop\automation\9 Smart Result dispatcher\output\reports"
os.makedirs(output_dir, exist_ok=True)
background_path=r"C:\Users\akank\Desktop\automation\9 Smart Result dispatcher\templates\pdf template.webp"

def generateReport(student: dict) -> str:
    filename=f"{student['name'].replace(' ', '_')}_report.pdf"
    path=os.path.join(output_dir, filename)
    
    c=canvas.Canvas(path, pagesize=A4)
    width, height=A4
    
    if os.path.exists(background_path):
        c.drawImage(background_path, 0, 0, width=width, height=height)
    c.setFont("Helvetica-Bold", 26)
    c.drawCentredString(width/2, height-40*mm, "Result Report")
    
    c.setFont("Helvetica", 14)
    left=30*mm
    y=height-60*mm
    c.drawString(left, y, f"Name: {student.get('name', '')}")
    y-=14*mm
    c.drawString(left, y, f"Class: {student.get('class', '')}")
    y-=14*mm
    c.drawString(left, y, f"Marks: {student.get('marks', '')}")
    y-=14*mm
    c.drawString(left, y, f"Attendance: {student.get('attendance', '')}")
    y-=14*mm
    c.drawString(left, y, f"Remark: {student.get('remark', '')}")  
    
    c.setFont("Helvetica-Oblique", 10)
    c.drawString(left, 20*mm, "Generated by Smart Result Dispatch System")
    
    c.showPage()
    c.save()
    return path
from reportlab.lib.pagesizes import A4
from reportlab.pdfgen import canvas
from reportlab.lib.units import mm
import os

output_dir=os.path.join(os.path.dirname(__file__), "..", "output", "reports")
os.makedirs(output_dir, exist_ok=True)
background_path=os.path.join(os.path.dirname(__file__), "templates", "pdf_template.png")

def generate_report(student: dict) -> str:
    filename=f"{student['name'].replace(' ', '_'
    )}_report.pdf"
    path=os.path.join(output_dir, filename)
    
    c=canvas.Canvas(path, pagesize=A4)
    width, height=A4
    
    if os.path.exists(background_path):
        c.drawImage(
            background_path,
            0,
            0,
            width=width,
            height=height,
            preserveAspectRatio=True,
            mask='auto'
        )
    c.setFont("Helvetica-Bold", 20)
    c.drawCentredString(width/2, height-40*mm, "Result Report")
    
    c.setFont("Helvetica", 12)
    left=30*mm
    y=height-60*mm
    c.drawCentredString(left, y, f"Name: {student.get('name', '')}")
    y-=10*mm
    c.drawCentredString(left, y, f"Class: {student.get('class', '')}")
    y-=10*mm
    c.drawCentredString(left, y, f"Marks: {student.get('marks', '')}")
    y-=10*mm
    c.drawCentredString(left, y, f"Attendance: {student.get('attendance', '')}")
    y-=10*mm
    c.drawCentredString(left, y, f"Remark: {student.get('remark', '')}")  
    
    c.setFont("Helvetica-Oblique", 10)
    c.drawString(left, 20*mm, "Generated by Smart Result Dispatch System")
    
    c.showPage()
    c.save()
    return path